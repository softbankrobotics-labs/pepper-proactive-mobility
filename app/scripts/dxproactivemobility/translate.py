def _get_audio_text(dict_language, param=None):
	text = ""
	if param and param.lower() in dict_language:
		text = dict_language[param.lower()]["audio"]
	if "common" in dict_language:
		text += " \pau=200\ " + dict_language["common"]["audio"] 
	return text

def _get_tablet_text(dict_language, param=None):
	text = ""
	if param and param.lower() in dict_language:
		text = dict_language[param.lower()]["tablet"]
	if "common" in dict_language:
		text += " \pau=200\ " + dict_language["common"]["tablet"] 
	return text

def say_audio_text(ALAnimatedSpeech, ALTextToSpeech, dict_text, param=None):
	if ALTextToSpeech and ALAnimatedSpeech:
		language = ALTextToSpeech.getLanguage()
		# if the language is handled by the application
		if language in dict_text:
			dict_language = dict_text[language]
			text = _get_audio_text(dict_language, param)
			# Say in the actual language
			ALAnimatedSpeech.say(text)
		else:
			# Change language to English
			ALTextToSpeech.setLanguage("English")
			# Say and reset to actual language
			dict_language = dict_text["English"]
			text = _get_audio_text(dict_language, param)
			ALAnimatedSpeech.say(text)
			ALTextToSpeech.setLanguage(language)

def notify(ALNotificationManager, ALTextToSpeech, dict_text, param):
	language = ALTextToSpeech.getLanguage() if ALTextToSpeech else "English"
	dict_language = dict_text[language]
	message = _get_tablet_text(dict_language, param)
	if ALNotificationManager:
		return ALNotificationManager.add({"message": message, "severity": "warning", "removeOnRead": True})
	return -1

def get_tablet_text(dict_text, param, language):
	if language in dict_text:
		dict_language = dict_text[language]
		if param.lower() in dict_language:
			return dict_language[param.lower()]["audio"]
	else:
		dict_language = dict_text["English"]
		if param.lower() in dict_language:
			return dict_language[param.lower()]["audio"]
